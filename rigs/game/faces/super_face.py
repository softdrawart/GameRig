class Rig(BoneUtilityMixin, old_super_face, MechanismUtilityMixin):


    def generate(self):

        self.orient_org_bones()
        all_bones, tweak_unique = self.create_bones()
        self.parent_bones(all_bones, tweak_unique)
        self.constraints(all_bones)
        jaw_prop, eyes_prop = self.drivers_and_props(all_bones)


        # Create UI
        all_controls = []
        all_controls += [ bone for bone in [ bgroup for bgroup in [ all_bones['ctrls'][group] for group in list( all_bones['ctrls'].keys() ) ] ] ]
        all_controls += [ bone for bone in [ bgroup for bgroup in [ all_bones['tweaks'][group] for group in list( all_bones['tweaks'].keys() ) ] ] ]

        all_ctrls = []
        for group in all_controls:
            for bone in group:
                all_ctrls.append( bone )

        controls_string = ", ".join(["'" + x + "'" for x in all_ctrls])
        #self.remove_quat_rot_mode({'ctrls': all_ctrls})

        return [ script % (
            controls_string,
            all_bones['ctrls']['jaw'][0],
            all_bones['ctrls']['eyes'][2],
            jaw_prop,
            eyes_prop )
            ]

        # for key in all_controls:
        #     print(key)
        #     self.remove_quat_rot_mode(key)

    def parent_bones(self, all_bones, tweak_unique):
        super().parent_bones(all_bones, tweak_unique)

        face_name = [ bone for bone in self.org_bones if 'face' in bone ].pop()

        for def_bone in all_bones["deform"]["all"]:
            parent = parent_orig = self.get_bone_parent(def_bone)

            while parent:                
                parent_def = deformer(strip_org(parent))

                if parent_def in self.obj.pose.bones and parent_def != def_bone:
                    parent = parent_def
                    break
                parent = self.get_bone_parent(parent)

            self.set_bone_parent(org(strip_def(def_bone)), parent_orig)
            self.set_bone_parent(def_bone, parent)
            

    def constraints(self, all_bones):
        super().constraints(all_bones)

        bones = self.obj.pose.bones

        for def_bone in all_bones["deform"]["all"]:
            constraints = bones[def_bone].constraints
            
            if constraints:
                for constraint in constraints:
                    move_constraint(bones[def_bone], bones[org(strip_def(def_bone))], constraint)

            if self.params.enable_scale:
                self.make_constraint(def_bone, 'COPY_TRANSFORMS', org(strip_def(def_bone)))            
            else:
                self.make_constraint(def_bone, 'COPY_LOCATION', org(strip_def(def_bone)))            
                self.make_constraint(def_bone, 'COPY_ROTATION', org(strip_def(def_bone)))



def add_parameters(params):
    """ Add the parameters of this rig type to the
        RigifyParameters PropertyGroup
    """

    # Setting up extra layers for the tweak bones
    ControlLayersOption.FACE_PRIMARY.add_parameters(params)
    ControlLayersOption.FACE_SECONDARY.add_parameters(params)

    params.enable_scale = bpy.props.BoolProperty(
        name="Scale",
        default=True,
        description="Deformation bones will inherit the scale of their ORG bones. Enable this only if you know what you are doing because scale can break your rig in the game engine"
    )


def parameters_ui(layout, params):
    """ Create the ui for the rig parameters."""

    ControlLayersOption.FACE_PRIMARY.parameters_ui(layout, params)
    ControlLayersOption.FACE_SECONDARY.parameters_ui(layout, params)

    r = layout.row()
    r.prop(params, "enable_scale")


